public class Calculator {
    

    public int subtract(int a, int b) {
        return a - b;
    }

    public float subtract(float a, float b) {
        return a - b;
    }

    public double subtract(double a, double b) {
        return a - b;
    }

    public long subtract(long a, long b) {
        return a - b;
    }
}
